// Grid dimensions
const int N = 20;
const int M = 40;

// Helper function to initialize the ncurses window
void initWindow() {
    initscr(); // Initialize ncurses
    cbreak(); // Disable line buffering
    noecho(); // Don't echo characters
    curs_set(0); // Hide cursor
}

// Helper function to close the ncurses window
void closeWindow() {
    endwin();
}

void rectangle(int y1, int x1, int y2, int x2)
{
    mvhline(y1, x1, 0, x2-x1);
    mvhline(y2, x1, 0, x2-x1);
    mvvline(y1, x1, 0, y2-y1);
    mvvline(y1, x2, 0, y2-y1);
    mvaddch(y1, x1, ACS_ULCORNER);
    mvaddch(y2, x1, ACS_LLCORNER);
    mvaddch(y1, x2, ACS_URCORNER);
    mvaddch(y2, x2, ACS_LRCORNER);
}

// Helper function to print the grid
void printGrid(const std::vector<std::vector<int>>& grid, int step) {
    clear(); // Clear the screen
    rectangle(0, 0, N+2, M*2+2);
    for (int row = 0; row < N; ++row) {
        for (int col = 0; col < M; ++col) {
            if (grid[row][col] == 1) {
                mvprintw(1 + row, 1 + col * 2, "█"); // Print live cell
                mvprintw(1 + row, 1 + col * 2 + 1, "█"); // Print live cell
            } else {
                mvprintw(1 + row, 1 + col * 2, "  "); // Print dead cell
            }
        }
    }
    mvprintw(N+2, M*2+2-3, "%d", step);
    refresh(); // Refresh the screen
}

// Function to update cell states
void updateGrid(std::vector<std::vector<int>>& grid) {
    std::vector<std::vector<int>> newGrid = grid;

    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < M; ++j) {
            int neighbors = 0;
            for (int di = -1; di <= 1; ++di) {
                for (int dj = -1; dj <= 1; ++dj) {
                    if (di == 0 && dj == 0) continue;
                    int ni = i + di;
                    int nj = j + dj;
                    if (ni >= 0 && ni < N && nj >= 0 && nj < M) {
                        neighbors += grid[ni][nj];
                    }
                }
            }

            int currentState = grid[i][j];
            if (currentState == 1 && (neighbors < 2 || neighbors > 3)) {
                newGrid[i][j] = 0; // Cell dies
            } else if (currentState == 0 && neighbors == 3) {
                newGrid[i][j] = 1; // Cell becomes alive
            } else {
                newGrid[i][j] = currentState; // Cell remains the same
            }
        }
    }

    // Update original grid with new values
    grid = newGrid;
}

